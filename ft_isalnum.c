#include "libft.h"

int	ft_isalnum(int c)
{
	int res;

	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
	|| (c >= 'a' && c <= 'z'))
	{
		res = 1;
	}
	else
	{
		res = 0;
	}
	return (res);
}
