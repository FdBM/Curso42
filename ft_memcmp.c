#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int		i;
	char	*cs1;
	char	*cs2;

	i = 0;
	cs1 = (char *)s1;
	cs2 = (char *)s2;
	while (i < (int)n)
	{
		if (cs1[i] != cs2[i])
			return ((unsigned char)cs1[i] - (unsigned char)cs2[i]);
		i++;
	}
	return (0);
}
