#include "libft.h"

void	*ft_memset(void *ptr, int x, size_t n)
{
	int				i;
	unsigned char	*c;

	c = ptr;
	i = 0;
	while (i < (int)n)
	{
		c[i] = x;
		i++;
	}
	return (ptr);
}
